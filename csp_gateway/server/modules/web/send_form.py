from typing import TYPE_CHECKING, Any, Dict, List, Set, get_args, get_origin

from pydantic import Field

from csp_gateway.server import ChannelSelection, GatewayChannels, GatewayModule

# separate to avoid circular
from csp_gateway.server.web import GatewayWebApp

if TYPE_CHECKING:
    from csp_gateway.server.web.spaday_ui import GatewayUI


class MountSendForm(GatewayModule):
    """Spaday UI: a "send data to a channel" panel that generates an input form per sendable channel.

    This is a **presentation** module for the spaday UI provider only — its `ui()` hook is a no-op under
    the default (React) UI, and it mounts no routes and touches no data (it relies on the send REST
    endpoints that `MountRestRoutes` provides). It is deliberately separate from `MountRestRoutes` so that
    form/appearance configuration never conflates with REST routing, which is independent of the UI.

    A form is generated for each channel that has a send adapter (registered via `add_send_channel`)
    and is included in `mount_send`; dict-basket channels get a key selector (the POST targets
    `/send/{channel}/{key}`). Controls and native validation are generated by spaday's `form()` from
    each channel struct's JSON schema; `form_overrides` relabels or drops individual fields without
    touching the data or routing layers.
    """

    mount_send: ChannelSelection = Field(
        default_factory=ChannelSelection,
        description=(
            "Send channels to render a form for. Should match the send channels `MountRestRoutes` actually "
            "mounts (its `mount_send` / `force_mount_all`), so the UI never shows a form that POSTs to a "
            "route that was intentionally not mounted. Defaults to all sendable channels."
        ),
    )

    form_overrides: Dict[str, Dict[str, Any]] = Field(
        default_factory=dict,
        description=(
            "Per-field overrides for the generated send form, keyed by 'channel.field' (specific channel) "
            "or 'field' (any channel). Each value is a mapping with 'label' (relabel) and/or 'exclude' "
            "(drop the field); the control itself is chosen by spaday's form() from the channel struct's "
            "JSON schema. Presentation-only and transport-agnostic."
        ),
    )

    def connect(self, channels: GatewayChannels) -> None:
        # NO-OP: this module contributes UI only.
        ...

    def rest(self, app: GatewayWebApp) -> None:
        # NO-OP: no REST routes; the form posts to the send routes mounted by MountRestRoutes.
        ...

    def ui(self, app: "GatewayUI") -> None:
        # Build a "send data to a channel" panel from the genuinely sendable channels (those with a
        # send adapter registered via `add_send_channel`). Dict baskets get a key selector.
        from csp_gateway.server.web.spaday_ui import Region, SendSpec

        web_app = app.web_app
        channels = web_app.gateway.channels
        # Only channels whose send route is mounted (per `mount_send`) — otherwise the form would POST
        # to a `/send/{channel}` route that `MountRestRoutes` intentionally did not mount.
        allowed = set(self.mount_send.select_from(web_app.gateway.channels_model))

        # Collect the selectable keys per dict-basket channel from its keyed send adapters.
        keys_by_channel: Dict[str, List[str]] = {}
        for channel, indexer in channels._send_channels.keys():
            if indexer is not None and web_app._is_dict_basket_field(channel):
                keys_by_channel.setdefault(channel, []).append(getattr(indexer, "name", None) or str(indexer))

        forms = []
        seen: Set[str] = set()
        for channel, _indexer in channels._send_channels.keys():
            if channel in seen or channel not in allowed:
                continue
            seen.add(channel)
            model = web_app._get_field_pydantic_type(channel)
            if model is None:
                continue
            if get_origin(model) is list:
                model = get_args(model)[0]
            forms.append(
                SendSpec(
                    channel=channel,
                    url=f"{web_app.settings.API_STR}/send/{channel}",
                    model=model,
                    keys=keys_by_channel.get(channel, []),
                    overrides=self._overrides_for(channel),
                )
            )

        if forms:
            app.add(Region.DRAWER_BOTTOM, app.send_panel(forms))
            seeds: Dict[str, Any] = {"send_channel": forms[0].channel}
            for spec in forms:
                if spec.keys:
                    seeds[f"send_key_{spec.channel}"] = spec.keys[0]
            app.seed_store(**seeds)

    def _overrides_for(self, channel: str) -> Dict[str, Any]:
        """Resolve `form_overrides` for one channel: 'channel.field' entries win over bare 'field' ones."""
        resolved: Dict[str, Any] = {}
        for key, cfg in self.form_overrides.items():
            if "." not in key:
                resolved[key] = cfg
        for key, cfg in self.form_overrides.items():
            if "." in key:
                ch, field_name = key.split(".", 1)
                if ch == channel:
                    resolved[field_name] = cfg
        return resolved
